#include <stdio.h>

int main() {

    int x = 5;
    int y = x / 2;

    printf("The value of x is %d", x);
    printf("\nThe value of y is %d\n", y);

    printf("%i\n", 9000); // %i is used to print integers
    printf("%f\n", 3.14); // %f is used to print floating point numbers
    printf("%c\n", 'a'); // %c is used to print characters
    printf("%s\n", "Hello"); // %s is used to print strings

    return 0;
}

