#include <stdio.h>

int main() {
    printf("Hello, World!\n"); // \n is the newline character

    printf("1\t2\t3\n4\t5\t6\n7\t8\t9"); // \t is the tab character

    printf("\n\"C is cool\""); // \" is the double quote character

    return 0;
}

